// Helper to get default settings for a priority level
export const getPrioritySettings = (priority: string) => {
  switch (priority) {
    case 'urgent':
      return {
        channels: ['popup', 'email', 'sms'] as ('popup' | 'email' | 'sms')[],
        digestEnabled: false,
        popupSound: true,
      };
    case 'important':
      return {
        channels: ['popup', 'email'] as ('popup' | 'email' | 'sms')[],
        digestEnabled: false,
        popupSound: false,
      };
    case 'normal':
      return {
        channels: ['email'] as ('popup' | 'email' | 'sms')[],
        digestEnabled: true,
        digestFrequency: '4-hours' as 'hourly' | '2-hours' | '4-hours' | 'beginning-of-day' | 'end-of-day' | 'weekly',
        popupSound: false,
      };
    case 'low':
      return {
        channels: ['email'] as ('popup' | 'email' | 'sms')[],
        digestEnabled: true,
        digestFrequency: 'beginning-of-day' as 'hourly' | '2-hours' | '4-hours' | 'beginning-of-day' | 'end-of-day' | 'weekly',
        popupSound: false,
      };
    case 'off':
      return {
        channels: [] as ('popup' | 'email' | 'sms')[],
        digestEnabled: false,
        popupSound: false,
      };
    default:
      return {
        channels: ['email'] as ('popup' | 'email' | 'sms')[],
        digestEnabled: false,
        popupSound: false,
      };
  }
};